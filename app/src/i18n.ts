export function interpolate(template: string, values?: Record<string, string | number>) {
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}

export function translate(language: ResolvedLanguage, key: string, values?: Record<string, string | number>) {
  const template = messages[language][key] ?? messages.en[key] ?? key;
  return interpolate(template, values);
}

export function useT() {
  const resolvedLanguage = useUiStore((state) => state.resolvedLanguage);
  return useCallback(
    (key: string, values?: Record<string, string | number>) => translate(resolvedLanguage, key, values),
    [resolvedLanguage],
  );
}
